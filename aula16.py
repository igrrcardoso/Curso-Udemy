# if / elif / else
# se / se não se / se não

while True:

    entrada = input('digite "entrar" ou "sair": ')

    if entrada == 'entrar':
        print('Você entrou no sistema')
    elif entrada == 'sair':
        print('Você saiu do sistema')
    else:
        print('você não digitou "entrar" nem "sair"')
