# if / elif / else
# se / se não se / se não
condicao1 = True
condicao2 = False
condicao3 = True
condicao4 = False


if condicao1:
    print('condição 1')
elif condicao2:
    print('condição 2')
elif condicao3:
    print('condição 3')
elif condicao4:
    print('condição 4')
else:
    print('nenhuma condição foi satisfeita')


